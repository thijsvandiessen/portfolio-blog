/**
 * Adds two numbers together.
 * @param {int} a - The first number.
 * @param {int} b - The second number.
 * @return {int} The sum of the two numbers.
 */
export function add(a, b) {
  return a + b;
}

/**
 * Multiplies two numbers.
 * @param {int} a - The first number.
 * @param {int} b - The second number.
 * @return {int} The multiplication of the two numbers.
 */
export function multiply(a, b) {
  return a * b;
}

/**
 * Substracs the second number from the first number.
 * @param {int} a - The first number.
 * @param {int} b - The second number.
 * @return {int} The result of the substraction
 */
export function subtract(a, b) {
  return a - b;
}

/**
 * Divides the second number by the first number.
 * @param {int} a - The first number.
 * @param {int} b - The second number.
 * @return {int} The result of the devision
 */
export function divide(a, b) {
  return a/b;
}
